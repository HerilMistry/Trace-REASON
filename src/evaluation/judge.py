import csv
import os
from src.llm import LLMFactory

class LLMJudge:
    def __init__(self, provider="groq"):
        self.llm = LLMFactory.create_provider(provider)
        self.scores_file = "outputs/evaluations/judge_scores.csv"
        os.makedirs(os.path.dirname(self.scores_file), exist_ok=True)
        self._init_csv()

    def _init_csv(self):
        if not os.path.exists(self.scores_file):
            with open(self.scores_file, "w", newline="") as f:
                writer = csv.writer(f)
                writer.writerow([
                    "enzyme", "model", "judge_model",
                    "scientific_accuracy", "faithfulness", "completeness",
                    "mechanistic_reasoning", "evidence_usage"
                ])

    def evaluate(self, enzyme: str, model: str, judge_model: str, explanation: str, evidence_summary: str) -> dict:
        prompt = f"""
        You are an expert scientific judge evaluating an enzyme explanation generated by an AI model.
        
        Enzyme: {enzyme}
        Evidence Summary:
        {evidence_summary}
        
        Explanation to evaluate:
        {explanation}
        
        Score the explanation on a scale of 1-5 (1=poor, 5=excellent) for the following criteria:
        1. Scientific Accuracy: Is the explanation scientifically sound?
        2. Faithfulness: Does it accurately reflect the provided evidence without hallucination?
        3. Completeness: Does it cover all key aspects (identity, kinetics, mutations, disease)?
        4. Mechanistic Reasoning: Does it explain HOW the enzyme works and causes disease?
        5. Evidence Usage: Does it effectively utilize the provided evidence?
        
        Respond with ONLY valid JSON:
        {{
            "scientific_accuracy": 0,
            "faithfulness": 0,
            "completeness": 0,
            "mechanistic_reasoning": 0,
            "evidence_usage": 0
        }}
        """
        
        scores = self.llm.structured_query(prompt, {
            "scientific_accuracy": "number",
            "faithfulness": "number",
            "completeness": "number",
            "mechanistic_reasoning": "number",
            "evidence_usage": "number"
        })
        
        # Save to CSV
        with open(self.scores_file, "a", newline="") as f:
            writer = csv.writer(f)
            writer.writerow([
                enzyme, model, judge_model,
                scores.get("scientific_accuracy", 0),
                scores.get("faithfulness", 0),
                scores.get("completeness", 0),
                scores.get("mechanistic_reasoning", 0),
                scores.get("evidence_usage", 0)
            ])
            
        return scores
